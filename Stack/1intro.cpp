// Stack is a abstract data type
// It is a logical data type
// It is a linear data type

// Stack has a specific way for insertion and deletion
// It is based on LIFO Last In First Out
// It is logical because it is implemented using the physical data structures like arrays and linked list ,
// it has no direct representation in memory like arrays and liked list.

/*
 *  Operations on stack
 *
 *  1. push --> insert at top
 *  2. pop --> delete from top
 *  3. top --> give the top element
 *  4. size --> give the no. of elements inside the stack
 *  5. isEmpty --> tell if stack is empty or not
 *
 *
 * */